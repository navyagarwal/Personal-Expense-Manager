    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int row_index = table.getSelectedRow();
        
        if(row_index != -1){
            int result = JOptionPane.showConfirmDialog(null, "Are you sure?", "Deletion Confirmation", JOptionPane.YES_NO_OPTION);
            if(result == JOptionPane.YES_OPTION){
                int id = (int)table.getValueAt(row_index, 0);
                try{
                    DB.dbConnect.st.executeUpdate("delete from spendings where sid="+id);
                    JOptionPane.showMessageDialog(null, "Successfully removed!");
                    getEntries();
                }catch(Exception ex){
                    JOptionPane.showMessageDialog(null, ex);
                }
            }
            
        }
    }//GEN-LAST:event_jButton3ActionPerformed

