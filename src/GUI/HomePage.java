    private void displayCategory(){
        try{
            Category.removeAllItems();
            ResultSet rs = DB.dbConnect.st.executeQuery("select * from category_info");
            while(rs.next()){
                Category.addItem(rs.getString("category"));
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    private void getEntries(){
        try{
            javax.swing.table.DefaultTableModel dtm = (javax.swing.table.DefaultTableModel)table.getModel();
            int row_count = dtm.getRowCount();
            while(row_count-- != 0){
                dtm.removeRow(0);
            }
            
            java.time.LocalDate current_date = java.time.LocalDate.now();
            java.time.LocalDate previous_date = current_date.minusDays(30);
            ResultSet rs = DB.dbConnect.st.executeQuery("select * from spendings where sdate<='"+current_date+"'and sdate>='"+previous_date+"'");
            
            int total = 0;
            while(rs.next()){
                int amt = rs.getInt("amount");
                total += amt;
                Object o[] = {rs.getInt("sid"), rs.getString("category"), rs.getDate("sdate"), amt};
                dtm.addRow(o);
            }
            
            TotalAmt.setText(total+"");
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }

